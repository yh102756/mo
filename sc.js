function showDuet() {
    for (let i = 0; i < duet.length; i++) {
        let songslen = duet[i].songs.length; // 노래 수
        let spannum = spanCalc(songslen); // 줄 수 계산
        htmlText += "<tr><th rowspan=\"" + spannum + "\">" + duet[i].singer.split("/")[0] + "</th>"; // 가수 이름

        for (let j = 0; j < songslen; j++) { // 노래들
            htmlText += "<td ";
            htmlText += (songsPerRow == 2) ? "style=\"width:100%;\"" : "style=\"width:15%;\"";
            htmlText += ">" + duet[i].songs[j].split("/")[0] + "</td>"; // 노래 제목

            // 한 줄에 표시할 노래 수에 따라 행 종료
            if (j % songsPerRow == songsPerRow - 1) {
                htmlText += "</tr>";
                if (j != songslen - 1) {
                    htmlText += "<tr>";
                }
            }
        }
        // 남은 노래가 있을 경우 행 종료
        if (songslen % songsPerRow != 0) {
            htmlText += "</tr>";
        }
    }
    htmlText += "</table>"; // 테이블 종료
}

function makeNewTable() {
    htmlText = "<table id=\"new\" align=\"center\"><tr><td style=\"background-color: rgb(50, 250, 150);\"";
    let newLength = newUpdate.length; // 새로운 업데이트의 길이
    let newSpan = spanCalc(newLength) * 2; // rowspan 계산
    let half = songsPerRow / 2;
    let remainder = newLength % songsPerRow;

    // rowspan 조정
    if (remainder > 0 && remainder <= half) {
        newSpan = newSpan - 1;
    }
    htmlText += " rowspan=\"" + newSpan + "\">New</td>"; // "New" 제목 셀

    for (let i = 0; i < newLength; i++) {
        htmlText += "<td ";
        htmlText += (songsPerRow == 2) ? "style=\"width:34%;\"" : "style=\"width:29%;\"";
        htmlText += ">" + newUpdate[i] + "</td>"; // 새로운 노래 제목

        // 한 줄에 표시할 노래 수에 따라 행 종료
        if (i % half == half - 1) {
            htmlText += "</tr>";
            if (i != newLength - 1) {
                htmlText += "<tr>";
            } 
        }
    }
}

