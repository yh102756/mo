function displayList(data) {
    const showElement = document.getElementById('show');
    showElement.innerHTML = ''; // 기존 내용을 지웁니다.

    data.forEach(item => {
        const p = document.createElement('p');
        p.textContent = `${item.artist} - ${item.title}`; // 예시: 가수 - 제목
        showElement.appendChild(p);
    });
}

function search(query) {
    const showElement = document.getElementById('show');
    const items = showElement.getElementsByTagName('p');

    for (let i = 0; i < items.length; i++) {
        const itemText = items[i].textContent.toLowerCase();
        if (itemText.includes(query.toLowerCase())) {
            items[i].style.display = ''; // 보이게
        } else {
            items[i].style.display = 'none'; // 숨기기
        }
    }
}

